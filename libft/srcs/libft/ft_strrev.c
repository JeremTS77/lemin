#include "libft.h"

char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	int		temp;

	i = 0;
	j = ft_strlen(str) - 1;
	temp = 0;
	while (i < j)
	{
		temp = str[i];
		str[i] = str[j];
		str[j] = temp;
		i++;
		j--;
	}
	return (str);
}
