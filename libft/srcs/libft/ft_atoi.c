#include "libft.h"

int		ft_atoi(const char *str)
{
	int		result;
	int		n;
	int		i;

	result = 0;
	i = 0;
	n = 1;
	if (!str || !*str)
		return (0);
	while (str[i] == ' ' || (str[i] > 8 && str[i] < 14))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			n = -1 * n;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = (result * 10) + (str[i] - '0');
		i++;
	}
	return (result * n);
}
